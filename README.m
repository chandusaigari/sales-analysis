
**🧥 Men’s Clothing Sales & Customer Insights Analysis
📌 Project Overview**

This project analyzes men’s clothing sales data (6,000 records) collected over a 3-month period (January – March).

The objective is to uncover actionable insights that help stakeholders improve:

Revenue growth
Customer satisfaction
Operational efficiency
Product performance

The analysis combines:

Python → Data Cleaning & Preprocessing
Power BI → Exploratory Data Analysis (EDA) & Interactive Dashboard
🎯 Business Problem

Stakeholders faced challenges in:

Identifying top-performing products and categories
Understanding regional performance differences
Improving delivery efficiency and customer satisfaction
Optimizing payment methods and customer experience
Aligning product attributes (size/color) with customer preferences
📊 Dataset Information
Total Records: 6,000
Time Period: January to March
Domain: E-commerce (Men’s Clothing)
Key Features:
Product Category
City / Location
Delivery Days
Customer Ratings
Payment Method
Order Value / Revenue
Product Attributes (Size, Color)
🛠️ Tools & Technologies Used
Python (Pandas, NumPy) → Data Cleaning & Transformation
Power BI → Dashboarding & Visualization
Excel/CSV → Data Source
🔄 Data Processing Workflow
Data Cleaning (Python)
Handled missing values
Removed duplicates
Treated outliers
Standardized formats
Feature Engineering
Created KPIs (Revenue, Avg Rating, Delivery Time)
Derived categorical insights (City, Product, Payment)
Exploratory Data Analysis (Power BI)
Built interactive dashboards
Created cross-filter visuals for deeper insights
📈 Key Insights
🛍️ Product & Category Performance
Identified top revenue-generating categories
Certain products contributed disproportionately to total sales
High-volume products ≠ High-revenue products (pricing impact)
🌍 Geographical Analysis
Top-performing cities showed:
Higher order frequency
Better customer ratings
Cities with longer delivery times had lower satisfaction
🚚 Delivery & Customer Satisfaction
Strong correlation between:
Delivery Days ↑ → Ratings ↓
Faster delivery significantly improves customer experience
💳 Payment Method Insights
Certain payment methods were:
More frequently used
Associated with higher order values
Payment preference varied across regions
👕 Product Attributes (Size & Color)
Specific sizes/colors had:
Higher sales volume
Better ratings
Indicates customer preference patterns
